#konversi celcius

print("PROGRAM KONVERSI CELCIUS KE SEMUA METERAN SUHU\n===================================")
celcius = float(input("masukkan suhu celcius :"))
print("suhu =",celcius,"c")
reamur = float(4 / 5 * celcius)
fahrenheit = float(9 / 5 * celcius + 32)
kelvin = float(celcius + 273.15)

print("reamur =",reamur,"r")
print("fahrenheit =",fahrenheit,"f")
print("kelvin =",kelvin,"k")