#konversi reamur

print("PROGRAM KOVERSI CELCIUS KE SEMUA METERAN SUHU\n===================================")
reamur = float(input("masukkan suhu reamur :"))
print("suhu =",reamur,"r")

celcius = float(5 / 4 * reamur)
fahrenheit = float(9 / 4 * reamur + 32)
kelvin = float(5 / 4 * reamur + 273.15)

print("celcius =",celcius,"c")
print("fahrenheit =",fahrenheit,"f")
print("kelvin =",kelvin,"k")